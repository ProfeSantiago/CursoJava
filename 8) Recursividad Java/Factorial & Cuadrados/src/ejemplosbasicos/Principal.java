    public int Cuadrado(int numero){
        return numero * numero;        
    }
    
    public void Calcula_Cuadrados (){
        for (int i = 1 ; i <= Integer.valueOf(Numero_Txt.getText()); i++){
            Resultados.setText(Resultados.getText() + " " + String.valueOf(Cuadrado(i)));
        }
    }
               
    public long Factorial(int numero){
        if (numero <= 1){
            return 1;
        }else{
             return numero * Factorial(numero - 1);
        }              
    }
